print("OR")
# Local Or
print(True and True)
print(True and False)
print(False and False)

exp = (5 == 5) or (7 == 6)
print(exp)

exp = (10 > 5) or (4 < 8)
print(exp)

exp2 = (10 > 20) or (40 < 30)
print(exp2)

