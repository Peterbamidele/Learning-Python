## Python Logical Operator

print("Logical And")
# Logical And
print(True and True)
print(True and False)
print(False and False)

Var = (5 == 5) and (4 == 4)
print(Var)

Var = (5 > 5) and (4 == 4)
print(Var)

x = 4
Var = (x > 5) and (8 < x)
print(Var)

