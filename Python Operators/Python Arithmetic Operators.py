## Python Arithmetic Operators

# 1. Addition Operators
x = 10
y = 5
print(x + y)

X = "Hello" + "World"
print(X)

print()

# 2. Subtraction Operators
x = 10
y = 5
print(x - y)

print()

# 3. Multiplication Operator
x = 10
y = 5
print(x * y)

print()

# 4. Division Operator
x = 10
y = 5
print(x / y)

print()

# 5. Exponentiation Operators
x = 2 ** 3
print(x)

print()

# 6. Reminder Operator
X = 5 % 2
print(X)

# 7. Floor Division Operator
X = 7 // 2
print(X)

