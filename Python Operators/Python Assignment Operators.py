## Python Collections Operators

# Addition Assignment
x = 4
x += 3
print(x)

print()

# Subtraction Assignment
x = 4
x -= 3
print(x)

print()

# Multiplication Assignment
x = 4
x *= 3
print(x)

print()

# Division Assignment
x = 6
x /= 2
print(x)

print()

# Exponentiation Assignment
x = 2
x **= 3
print(x)

print()

#  Remainder Assignment
x = 5
x %= 2
print(x)

print()

# Floor Division Assignment
x = 5
x //= 2
print(x)
