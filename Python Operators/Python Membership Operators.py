## Python Membership Operators
"""
Membership operator are used to check id=f a sequence is present
in an Object like Strings,lists etc
"""

# in Operator
txt = "love python programming"
print("love" in txt)

print()

txt = "love python programming"
print("Hello" in txt)

print()

fruits = ["bananas", "apples", "grape", "carrot" "lemons"]
print("grape" in fruits)
print("orange" in fruits)

print()

print("The not in Operator")
txt = "i love fruits"
print("vegetable" not in txt)

fruits = ["bananas", "apples", "grape", "carrot" "lemons"]
print("orange" not in fruits)
print("apples" not in fruits)
